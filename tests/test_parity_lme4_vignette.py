"""Parity tests against lme4 canonical vignette datasets.

Covers three models from the lme4 paper / vignette (Bates et al. 2015):

  Dyestuff   :  Yield     ~ 1           + (1         | Batch)    single RE
  sleepstudy :  Reaction  ~ Days        + (Days       | Subject)  corr. slopes
  Pastes     :  strength  ~ 1           + (1          | batch/cask) nested RE

Fixtures generated by:  tests/fixtures/gen_lme4_vignette.R

Acceptance criteria (from CLAUDE.md):
  - Fixed effects abs_diff < 1e-4
  - Variance components rel_diff < 5%
  - BLUP correlation > 0.99
  - Conditional residual correlation > 0.999
"""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

import interlace

FIXTURES = Path(__file__).parent / "fixtures"


# ===========================================================================
# Dyestuff  —  Yield ~ 1 + (1 | Batch)
# ===========================================================================


@pytest.fixture(scope="module")
def dyestuff_data() -> pd.DataFrame:
    return pd.read_csv(FIXTURES / "lme4_dyestuff_data.csv")


@pytest.fixture(scope="module")
def dyestuff_ref() -> dict:
    return json.loads((FIXTURES / "lme4_dyestuff_results.json").read_text())


@pytest.fixture(scope="module")
def dyestuff_fit(dyestuff_data):
    return interlace.fit("Yield ~ 1", data=dyestuff_data, groups="Batch")


def test_dyestuff_intercept(dyestuff_fit, dyestuff_ref):
    r_val = dyestuff_ref["fe_params"]["(Intercept)"]
    il_val = dyestuff_fit.fe_params["Intercept"]
    diff = abs(il_val - r_val)
    assert diff < 1e-4, (
        f"Intercept abs_diff={diff:.2e} (interlace={il_val:.4f}, R={r_val:.4f})"
    )


def test_dyestuff_batch_variance(dyestuff_fit, dyestuff_ref):
    vc = dyestuff_ref["vc_table"]
    r_var_batch = next(row["vcov"] for row in vc if row["grp"] == "Batch")
    il_var_batch = dyestuff_fit.variance_components["Batch"]
    rel_diff = abs(il_var_batch - r_var_batch) / r_var_batch
    assert rel_diff < 0.05, (
        f"Batch variance rel_diff={rel_diff:.2%} "
        f"(interlace={il_var_batch:.4f}, R={r_var_batch:.4f})"
    )


def test_dyestuff_residual_variance(dyestuff_fit, dyestuff_ref):
    vc = dyestuff_ref["vc_table"]
    r_var_resid = next(row["vcov"] for row in vc if row["grp"] == "Residual")
    il_var_resid = dyestuff_fit.scale
    rel_diff = abs(il_var_resid - r_var_resid) / r_var_resid
    assert rel_diff < 0.05, (
        f"Residual variance rel_diff={rel_diff:.2%} "
        f"(interlace={il_var_resid:.4f}, R={r_var_resid:.4f})"
    )


def test_dyestuff_blups(dyestuff_fit, dyestuff_ref):
    r_blups = dyestuff_ref["ranef"]["Batch"]["(Intercept)"]
    labels = sorted(r_blups.keys())
    r_arr = np.array([r_blups[g] for g in labels])
    il_arr = np.array([dyestuff_fit.random_effects["Batch"][g] for g in labels])
    corr = np.corrcoef(r_arr, il_arr)[0, 1]
    assert corr > 0.99, f"Batch BLUP correlation={corr:.4f} < 0.99"


def test_dyestuff_residuals(dyestuff_fit, dyestuff_ref):
    r_resid = np.array(dyestuff_ref["resid_cond"])
    corr = np.corrcoef(dyestuff_fit.resid, r_resid)[0, 1]
    assert corr > 0.999, f"Residual correlation={corr:.6f} < 0.999"


