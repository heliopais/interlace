def test_pastes_blups_cask_batch(pastes_fit, pastes_ref):
    # lme4 labels the nested levels as "cask:batch" (e.g. "a:A");
    # interlace stores them as "batch:cask" (e.g. "A:a") — flip for alignment.
    r_blups = pastes_ref["ranef"]["cask:batch"]["(Intercept)"]
    il_blups = pastes_fit.random_effects["batch:cask"]
    # Reverse each lme4 label "a:A" → "A:a" to match interlace convention
    labels = sorted(il_blups.keys())
    il_arr = np.array([il_blups[g] for g in labels])
    r_arr = np.array([r_blups[":".join(reversed(g.split(":")))] for g in labels])
    corr = np.corrcoef(r_arr, il_arr)[0, 1]
    assert corr > 0.99, f"batch:cask BLUP correlation={corr:.4f} < 0.99"


